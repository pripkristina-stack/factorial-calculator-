while True:
    try:
        # Запрашиваем ввод у пользователя
        num = int(input("Введите положительное число: "))

        # Проверяем, положительное ли число
        if num > 0:
            print(f"Вы ввели положительное число: {num}")
            break  # Выходим из цикла, так как ввод корректен
        else:
            print("Ошибка: Введите число больше нуля.")
    except ValueError:
        # Обработка случая, если пользователь ввел не число
        print("Ошибка: Это не целое число. Попробуйте еще раз.")